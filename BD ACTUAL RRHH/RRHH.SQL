CREATE TABLE DEPART(
  DEPT_NO NUMBER(2) NOT NULL,
  DNOMBRE VARCHAR2(14),
  LOC VARCHAR2(14),
  CONSTRAINT PK_DEPART PRIMARY KEY(DEPT_NO)
);

CREATE TABLE EMPLE(
  EMP_NO    NUMBER(4),
  APELLIDO  VARCHAR2(10) NOT NULL UNIQUE,
  OFICIO    VARCHAR2(10),
  DIR       NUMBER(4) NOT NULL,
  FECHA_ALT DATE,
  SALARIO   NUMBER(10),
  COMISION  NUMBER(10),
  DEPT_NO   NUMBER(2) NOT NULL,
  CONSTRAINT PK_EMPLE PRIMARY KEY (EMP_NO),
  CONSTRAINT FK_EMPLE_EMPLE 
       FOREIGN KEY (DIR) REFERENCES EMPLE,
  CONSTRAINT FK_EMPLE_DEPART
       FOREIGN KEY (DEPT_NO) REFERENCES DEPART
);
